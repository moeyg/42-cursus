#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*dest;
	unsigned char	cache;

	dest = b;
	cache = (unsigned char)c;
	while (len--)
		*dest++ = cache;
	return (b);
}
