#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*cache;
	unsigned char	find;

	cache = (unsigned char *)s;
	find = (unsigned char)c;
	while (n--)
	{
		if (*cache == find)
			return ((void *)cache);
		cache++;
	}
	return ((void *)0);
}
